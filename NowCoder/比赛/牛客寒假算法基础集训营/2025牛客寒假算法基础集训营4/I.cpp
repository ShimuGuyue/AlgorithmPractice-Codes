void Solve(void)
{
	int n;
	cin >> n;
	string s;
	cin >> s;
	int ans = 0;
	int count_u = 0;
	for (int i = 0; i < n - 7; ++i)
	{	//uwawauwa
		if (s[i]   == 'u' && s[i+1] == 'w' && s[i+2] == 'a' && s[i+3] == 'w' &&
			s[i+4] == 'a' && s[i+5] == 'u' && s[i+6] == 'w' && s[i+7] == 'a')
			ans += count_u;
		if (i - 1 >= 0 && s[i - 1] == 'u')
			++count_u;
	}
	cout << ans << endl;
}