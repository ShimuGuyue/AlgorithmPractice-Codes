void Solve(void)
{
	int n, m;
	cin >> n >> m;
	vector<int> as(n);
	for (auto &a : as)
	{
		cin >> a;
	}
	sort(as.begin(), as.end());
	int l = *std::min_element(as.begin(), as.end());
	int r = 1e9;
	while (l < r)
	{
		int mid = (l + r + 1) / 2;
		if (Check(as, mid, m))
			l = mid;
		else
			r = mid - 1;
	}
	int ans = l;
	cout << ans << endl;
} 

bool Check(vector<int> &as, int k, int m)
{
	int n = as.size();
	int count = 1;
	int len = 0;
	for (int i = 1; i < n; ++i)
	{
		len += as[i] - as[i - 1];
		if (len >= k)
		{
			len = 0;
			++count;
		}
	}
	return count >= m;
}