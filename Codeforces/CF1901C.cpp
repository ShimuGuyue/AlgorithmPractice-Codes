void Solve(void)
{
	int n;
	cin >> n;
	int min = 1e9, max = 0;
	for (int i = 0; i < n; ++i)
	{
		int a;
		cin >> a;
		min = std::min(min, a);
		max = std::max(max, a);
	}
	vector<int> anss;
	while (min != max)
	{
		int x;
		if (min & 1)
			x = 1;
		else
			x = 0;
		anss.push_back(x);
		min = (min + x) / 2;
		max = (max + x) / 2;
	}
	cout << anss.size() << endl;
	if (anss.size() <= n && anss.size() != 0)
	{
		for (int x : anss)
		{
			cout << x << " ";
		}
		cout << endl;
	}
}