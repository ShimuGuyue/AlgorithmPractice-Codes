void Solve(void)
{
	int n;
	cin >> n;
	vector<int> as(n);
	cin >> as;
	vector<int64_t> sums(n);
	sums[0] = as[0];
	for (int i = 1; i < n; ++i)
	{
		sums[i] = sums[i - 1] + as[i];
	}

	vector<int> count_odds(n);
	count_odds[0] = (as[0] & 1);
	for (int i = 1; i < n; ++i)
	{
		count_odds[i] = count_odds[i - 1] + (as[i] & 1);
	}

	vector<int> cost_values(n);
	cost_values[0] = 0;
	for (int i = 1; i < n; ++i)
	{
		if (count_odds[i] == 0)
			cost_values[i] = 0;
		else
		if (count_odds[i] == 1)
			cost_values[i] = 1;
		else
			cost_values[i] = (count_odds[i] - 2) / 3 + (count_odds[i] + 1) % 3;

	}

	for (int i = 0; i < n; ++i)
	{
		cout << sums[i] - cost_values[i] << " ";
	}
	cout << endl;
}