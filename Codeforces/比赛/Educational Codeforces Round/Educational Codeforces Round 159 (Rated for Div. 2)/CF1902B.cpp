void Solve(void)
{
	int64_t n, p, l ,t;
	cin >> n >> p >> l >> t;
	int64_t count_task = (n - 1) / 7 + 1;
	int64_t daily_max = l + 2 * t;
	int64_t flag = count_task / 2 * daily_max;
	if (flag >= p)
	{
		int64_t days = p % daily_max == 0 ? p / daily_max : p / daily_max + 1;
		n -= days;
	}
	else
	{
		p -= flag;
		n -= count_task / 2;
		count_task %= 2;
		if (count_task == 1)
		{
			p -= l + t;
			--n;
		}
		if (p > 0)
		{
			n -= p % l == 0 ? p / l : p / l + 1;
		}
	}
	cout << n << endl;
}